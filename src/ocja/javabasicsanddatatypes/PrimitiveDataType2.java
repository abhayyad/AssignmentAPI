package ocja.javabasicsanddatatypes;

public class PrimitiveDataType2 {

	public static void main(String[] args) {
		
		
		final int i3 = 10; // then final variable is implicitly casted to target type
		short s3 = i3; // hence i3 is implicitly type casted to short
		
		int i7 = 10;
		short s7 = (short) i7; // requires type casting 
		
	}

}
